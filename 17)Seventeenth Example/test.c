#include <stdio.h>
// დაადგინეთ წელიწადში რამდენი დღეა 365 თუ 366
// შესატანი მონაცემები - ერთი მთელი რიცხვი მაგ: 2020
// გამოსატანი მონაცემები - ერთი მთელი რიცხვი - 366

int main(){
    
    int year;

    printf("Input Year: ");
    scanf("%d", &year);

    if (((year % 4 == 0) && (year % 100 != 0)) || (year % 400 == 0))
        printf("%d ნაკიანი წელია ამიტომ ამ წელს არის 366 დღე\n", year);
    else
        printf("%d არ არის ნაკიანი წელია ამიტომ 365 დღეა\n", year);

    return 0;
}
